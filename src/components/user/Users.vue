<script>
export default {
  data() {
    return {
      // 获取用户列表的参数
      queryinfo: {
        query: '',
        pagenum: 1,
        pagesize: 2
      },
      userlist: [],
      total: 0
    }
  },
  created() {
    this.getUserList()
  },
  methods: {
    async getUserList() {
      const { data: res } = await this.$http.get('users', {
        params: this.queryinfo
      })
      console.log(res)
      if (res.meta.status !== 200) return this.$message('获取用户数据失败！')
      this.userlist = res.data.users
      this.total = res.data.users.total
    }
  }
}
</script>

